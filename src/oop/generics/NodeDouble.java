package oop.generics;

public class NodeDouble {
    private double data;
    private NodeDouble next;

    public NodeDouble() {
    }

    public double getData() {
        return data;
    }

    public void setData(double data) {
        this.data = data;
    }

    public NodeDouble getNext() {
        return next;
    }

    public void setNext(NodeDouble next) {
        this.next = next;
    }
}
